from dataclasses import dataclass


@dataclass(frozen=True)
class FieldDictionaryEntry:
    field_name: str
    source_header: str
    business_label: str
    data_type: str
    meaning: str
    allowed_usages: tuple[str, ...]
    result_label: str
    source_trace_label: str
    caveats: tuple[str, ...] = ()


def entry(
    field_name: str,
    source_header: str,
    business_label: str,
    data_type: str,
    meaning: str,
    allowed_usages: tuple[str, ...],
    result_label: str,
    source_trace_label: str,
    caveats: tuple[str, ...] = (),
) -> FieldDictionaryEntry:
    return FieldDictionaryEntry(
        field_name=field_name,
        source_header=source_header,
        business_label=business_label,
        data_type=data_type,
        meaning=meaning,
        allowed_usages=allowed_usages,
        result_label=result_label,
        source_trace_label=source_trace_label,
        caveats=caveats,
    )


